# Készítsünk egy Tuple objektumot
my_tuple = ("kutya", "tacsko", "fekete", 6)

# Vegyük ki az első elemét
print(my_tuple[0])

# Vegyük ki az első két elemét egy új tuple-ba
print(my_tuple[:2])

# Hozzunk létre egy új tuple-t, úgy, hogy a régihez hozzáadunk egyszere több elemet
my_tuple += (True, "játékos")
print(my_tuple)

# Számoljuk meg, hányszor van a "kutya" elem a tuple-ban
print(my_tuple.count("kutya"))

# Hányadik indexen található a "tacskó"?
print(my_tuple.index("tacsko"))

